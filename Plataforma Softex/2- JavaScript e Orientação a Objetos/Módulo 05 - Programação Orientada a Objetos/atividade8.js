//Lista de alguns métodos possiveis para manipulação de string: toUpperCase(), toLowerCase(), length e substring()

//Exemplificação de um código com uso desses métodos
const frase = "Hello World!"

console.log(frase.toUpperCase()) //Método que deixa a string em leitra maiuscula
console.log(frase.toLowerCase()) //Método que deixa a string em leitra miinuscula
console.log(frase.length); //Retorna o tamanho da string
console.log(frase.substring(0,5)) //Recebe como parâmetro o inicio e o fim desejado, serve para fazer extração de parte da string